add_action( 'elementor/widget/render_content', function( $output, $widget ) {
	//make sure its an image box widget
	if ( 'image-box' !== $widget->get_name() ) {
		return $output;
	}
	$settings = $widget->get_settings_for_display();
	// Wrap paragraph in link
	$output = preg_replace( '/<p[^>]+>([^<]+)<\/p>/i',
		'<a ' . $widget->get_render_attribute_string( 'link' ) . '>\0</a>', $output );
	return $output;
}, 10, 2 );

